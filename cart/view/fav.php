			},
		};

		function addMarker(feature) {
			var marker = new google.maps.Marker({
				position: feature.position,
				icon: icons[feature.type].icon,
				map: map
			});
		}

		var features = [
			{
				position: new google.maps.LatLng(-33.916185, 151.222110),
				type: 'property'
			}
		];

